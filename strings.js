let greetings="Hello";
let response="There";

// indexOf
console.log(greetings.indexOf("l"))
console.log(greetings.lastIndexOf("l"))

// concat
console.log(greetings.concat(response))
console.log(greetings+ " " +response)

// index
// charAt
console.log(response.charAt(3))
console.log(response[2])

// toLocale
console.log(response.toLocaleLowerCase())
console.log(response.toLocaleUpperCase())

// uppercase
console.log(response.toUpperCase())

// lowercase
console.log(response.toLowerCase())

// slice
console.log(response.slice(2,4))

// substr
console.log(response.substr(1,4))

// includes
console.log(response.includes("s"))
console.log(response.includes("re"))